		<p>We at Unfiction have decided that we have only been providing you with half of the fun in the world of ARGs, as our forum has been considered Out of Game since its inception. Well, damn were we ever wrong about that! So to make up for the last three years of meta-status, the next three years of Unfiction will be held ENTIRELY IN-GAME!

		<p>That's right! No longer will you have to worry about whether or not this character or that can see what is posted to UF, because EVERYTHING AT UF will be KNOWN TO ALL GAME CHARACTERS ANYWHEN AND EVERYWHERE. Pay special attention if a game character you meet in the future seems to be unaware of some trivial tidbit you picked up on UF, even if it's from a completely different game...that just means he or she (or it) is trying to TRICK YOU! DO NOT BELIEVE ANYTHING THIS CHARACTER SAYS FROM THEN ON WITHOUT CORROBORATION FROM A TRUSTED SOURCE. Sources that can be trusted are usually easy to spot when they advertise free money for little to no return effort! </p>

		<p>P.S. Since UF is in-game now and you, the collective players, are obviously NOT in-game (being players), your posting and other participation privileges are suspended for the duration. But this is clearly a tiny price to pay for the years of passive entertainment upon which you are about to embark!</p>

		<p>We know this is all a little hard to understand, so please <strong><a href="commercial.html" target="commercial1" onClick="ufPop('commercial1','commercial.html',620,500); return false;">view our commercial</a></strong>, which explains everything to the fullest!</p>

