							<h2>Boogers</h2>
	<h3 title="published: fri. apr. 1, 2005"><a href="faq.php" title="Permanent link to this article">Frequently Asked Questions</a></h3>
	<div class="entryInfo clear">
		<span class="posted">fri. apr. 1, 2005 <a href="index.php">You don't Get Comments</a> [0]</span></div>
	
		<p><strong>Who comes up with this stuff?</strong></p>

		<p>A carefully trained and monitored team of minions, all of whom work for the sheer love of it and not because of any  potential blackmail material kept in secret files in an underground vault at Unfiction World Headquarters, no sirree.</p>

		<p><strong>Are you on crack?</strong></p>

		<p>We prefer heroin.</p>

		<p><strong>What happened to the forum?</strong></p>

		<p>We're not really history buffs, but Mrs. Randall told us in 8th grade that the Visigoths invaded Rome and pillaged and  rampaged, so we're going to go with that.</p>

		<p><strong>What are you going to do with my information?</strong></p>

		<p>If it leads to the recovery of the secret documents, we'll put your payment in the usual deaddrop by midnight.  Otherwise,  fuck if we know.</p>

		<p><strong>What is a Narcissistic Personality Disorder?</strong></p>

		<p>According to the <strong><a href="http://en.wikipedia.org/wiki/Narcissistic_personality_disorder">Wikipedia</a></strong>, it is a  "personality disorder that is characterized by extreme feelings of self-importance, a high need for admiration, and a lack of  empathy."</p>

		<p><strong>What is the Matrix?</strong></p>

		<p>Go ask the Metaurchins.</p>

		<p><strong>Where's Waldo?</strong></p>

		<p>In the Unfiction World HQ cafeteria, enjoying a pudding cup.</p>

		<p><strong>Why do birds suddenly appear every time you are near?</strong></p>

		<p>It could be those breadcrumbs SpaceBass keeps in his undies.</p>

		<p><strong>When will the insanity end?</strong></p>

		<p>When we earn enough to buy our own private island, or 00:00:00 PST, whichever comes first.</p>

		<p><strong>Is this really an FAQ?</strong></p>

		<p>We ask ourselves that quite a lot, so yes, yes it is.</p>

